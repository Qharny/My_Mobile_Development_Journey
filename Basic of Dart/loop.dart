// A for loop

void main() {
  for (int i = 1; i <= 5; i++) {
    print('Iteration $i');
  }



// A while loop

  int count = 1;
  while (count <= 5) {
    print('Iteration $count');
    count++;
  }


  // A do while loop
  int num = 1;
  do {
    print('Iteration $num');
    num++;
  } while (num <= 5);
}

